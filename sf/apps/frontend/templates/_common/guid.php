            <div id="uchet" class="uchet">
                <h3>Ведите учет!</h3>
                <div align="justify">
                    Учитывайте движение Ваших денег, используя кнопку «Добавить операцию». Выберите счет, по которому были произведены расходы, получены доходы или совершен перевод. Выберите категорию, к которой относится данная операция. Введите сумму операции и выберите дату, в которую она была совершена. А для более детального учета используйте Комментарий и Теги (отметки для упрощения поиска похожих операций и составления отчетов по ним)
                    <br>
                    <br>
                    Пример: <i>Если Вы хотите учесть в системе покупку бензина для автомобиля при оплате банковской карточкой – укажите сумму и дату, выберите счет «Карточка», тип операции «Расход», категория «Автомобиль», подкатегория «Бензин», в комментарии можете указать, сколько литров бензина было приобретено или какой конкретно автомобиль заправляли. Используя Теги Вы сможете например, разделить расходы на личные и служебные, просто напишите в поле теги «Личные расходы», если заправляли свой автомобиль или «Служебные расходы», если заправляли служебный автомобиль.</i>
                    <br>
                    <br>
                    Меню: Ввод операции доступен с любой страницы личного кабинета сайта.

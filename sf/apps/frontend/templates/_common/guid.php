                    <br>
                    <br>
                    Меню: В личном кабинете наведите указатель мыши на вкладку «Счета», в появившемся подменю щелкните «Категории», на открывшейся странице щелкните «Добавить новую категорию». Или наведите указатель мыши на существующую категории и щелкните во всплывающем меню, на значок «Редактировать» или «Удалить». После заполнения \ редактирования всех параметров категории, нажмите «Сохранить».

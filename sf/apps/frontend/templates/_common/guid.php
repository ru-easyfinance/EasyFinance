            <div id="categ" class="categ">
                <h3>Отредактируйте или создайте категории</h3>
                <div align="justify">
                    Зарплата, премия, еда, одежда, расходы на автомобиль – это все категории, по которым можно объединять Ваши доходы или расходы, осуществлять планирование. Система уже содержит в себе стандартный набор категорий, Вы можете удалить или отредактировать существующие категории, а также добавить свои собственные. При создании категории, обязательно выберите для нее название и тип. Добавьте из списка наиболее подходящую системную категории (Это позволит системе в дальнейшем предлагать Вам советы и рекомендации).
                    <br>
                    <br>
                    Также вы может подчинить одну категорию другой, создав таким образом Подкатегорию.
                    <br>
                    <br>
